    //asigna los nombres al label
    public void setNames(String name1, String name2) {
        this.lblJug1.setText(name1 + " :");
        this.lblJug2.setText(name2 + " :");
    }

    //limpia la matriz
    public void clearMatrix() {
        for (JButton[] row : btnMatriz) {
            for (JButton column : row){
                column.setText("");
            }
        }
    }

    //asigna los char a la matriz
    public void setMatrix(char[][] matrix) {
        for (int i = 0; i < matrix.length; i++) {
            for (int j = 0; j < matrix[i].length; j++) {
                this.btnMatriz[i][j].setText(Character.toString(matrix[i][j]));
            }
        }
    }

    //setea de quien es el turno
    public void setWho(String name) {
        this.lblQuien.setText(name);
    }

  
    //setea los puntos en pantalla
    public void setPoint(int points, int points0) {
        this.lblJug1Points.setText(Integer.toString(points));
        this.lblJug2Points.setText(Integer.toString(points0));
    }
