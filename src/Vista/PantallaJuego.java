    //crea la matriz de botones, primero cree un JButton[][] más abajo donde están los atributos
    public void _init_() {
        //la matriz es de 3 x 3
        int x = 3, y = 3;
        
        this.btnMatriz = new JButton[y][x]; //instancio

        //indico el tipo diseño, es de red
        //el 3 y el 4 indican los espacios entre los botones
        pnlMatriz.setLayout(new GridLayout(y, x,3,4));
        //recorro la martiz
        for (int i = 0; i < y; i++) {
            for (int j = 0; j < x; j++) {
                
                btnMatriz[i][j] = new JButton();
                //asigno el color
                btnMatriz[i][j].setBackground(new Color(230,58,59));
                //dimensiones del boton
                btnMatriz[i][j].setPreferredSize(new Dimension(45,45));
                //tipo de letra y tamaño
                btnMatriz[i][j].setFont(new Font("Arial", Font.PLAIN, 40));
                //y la añado al pnl donde está el diseño de red
                pnlMatriz.add(btnMatriz[i][j]);
            }
        }

        
        
    }

